import joserodpt.realmurdermystery.api.events.RSWRoomStateChangeEvent;
import joserodpt.realmurdermystery.api.map.RSWMap;
import org.bukkit.Bukkit;

public class RSWEventsAPI {

    public void callRoomStateChange(RSWMap g) {
        Bukkit.getPluginManager().callEvent(new RSWRoomStateChangeEvent(g));
        g.updateSigns();
    }
}
